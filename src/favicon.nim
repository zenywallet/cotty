import macros
import strutils

type
  Hex = distinct string

proc toBytesFromHex(s: string): seq[byte] {.compileTime.} =
  if s.len mod 2 == 0:
    result = newSeqOfCap[byte](s.len div 2)
    for i in countup(0, s.len - 2, 2):
      result.add(strutils.fromHex[byte](s[i..i+1]))
  else:
    result = @[]

proc toBytes(x: Hex): seq[byte] {.compileTime.} = x.string.toBytesFromHex()

